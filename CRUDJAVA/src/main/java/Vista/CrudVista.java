    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // BOTON ACTUALIZAR
         // Obtiene los valores de los campos de texto
        String nombre = txtnombre.getText();
        int telefono = Integer.parseInt(txttelefono.getText());
        String fechaTexto = txtfecha.getText();
        // Obtén el ID del registro a actualizar desde un campo de entrada de texto
        int id = Integer.parseInt(txtid.getText());
        try {
            // Formato para convertir la cadena de texto en un objeto java.util.Date
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
            Date fecha = dateFormat.parse(fechaTexto);

            // Crea un objeto Datos con los valores
            Datos datos = new Datos();
            datos.setId(id);
            datos.setNombre(nombre);
            datos.setTelefono(telefono);
            datos.setFecha(fecha);

            // Crea una instancia de la clase CrudDAO para manejar la base de datos
            CrudDAO update = new CrudDAO();

            // Llama al método update de la clase CrudDAO para actualizar el registro
            update.update(datos);

            // Notifica al usuario que la actualización se realizó con éxito
            JOptionPane.showMessageDialog(this, "Registro actualizado", "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (ParseException e) {
            // Manejo de errores en caso de que la fecha ingresada no sea válida
            JOptionPane.showMessageDialog(this, "Error al actualizar el registro.", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // ELIMINAR REGISTROS
        // Obtiene el ID del registro que deseas eliminar
        int id = Integer.parseInt(txtid.getText());

        // Crea una instancia de la clase Datos con el ID
        Datos datos = new Datos();
        datos.setId(id);

        // Crea una instancia de la clase CrudDAO para manejar la base de datos
        CrudDAO delete = new CrudDAO();

        // Llama al método delete de la clase CrudDAO para eliminar el registro
        delete.delete(datos);

        // Notifica al usuario que el registro se eliminó con éxito
        JOptionPane.showMessageDialog(this, "Registro eliminado", "Éxito",
                JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btneliminarActionPerformed


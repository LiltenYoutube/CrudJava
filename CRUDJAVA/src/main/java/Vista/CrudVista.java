    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // insetar datos 
        //instacia de clase 
        CrudDAO insert = new CrudDAO();
        
        //cajas de texto
        String nombre = txtnombre.getText();
        int telefono = Integer.parseInt(txttelefono.getText());
        String fechaTexto = txtfecha.getText();
        
         try {
            // Formato para convertir la cadena de texto en un objeto java.util.Date
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
            Date fecha = dateFormat.parse(fechaTexto);

            // Ahora puedes utilizar 'fecha' para insertar en la base de datos
            // Asegúrate de que tu base de datos pueda manejar objetos java.util.Date correctamente.
            // Luego, puedes llamar al método de inserción en tu DAO pasando nombre, telefono y fecha como argumentos.
            // instanciamos ala clase datos que genero inteti class from data base
            Datos datos = new Datos(); 
            datos.setNombre(nombre);
            datos.setTelefono(telefono);
            datos.setFecha(fecha);

            // Llama al método de inserción del DAO
            insert.insertar(datos);
             JOptionPane.showMessageDialog(this, "Registro insertado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } catch (ParseException e) {
            e.printStackTrace();
            // Manejo de errores en caso de que la fecha ingresada no sea válida
        }
        
    }//GEN-LAST:event_btninsertActionPerformed

    private void btnselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselectActionPerformed
        // INSTANCIA DE CLASE DAO
        CrudDAO select = new CrudDAO();
        //modelo de la tabla
        DefaultTableModel model = new DefaultTableModel ();
        //agregar columnas al modelo de la tabla
        model.addColumn("ID");
        model.addColumn("NOMBRE");
        model.addColumn("TELEFONO");
        model.addColumn("FECHA");
        //llamar al metodo selectdatos de curddao para obtner los datos de la basede datos
         List<String[]> data = select.selectDatos();
         //agregar los datos de base datos y agg ala tabla vista
         for (String[] row : data) {
         model.addRow(row);
         }
        //asignar el componete de la tabla en la interfaz grafica
        jTable3.setModel(model);
    }//GEN-LAST:event_btnselectActionPerformed

